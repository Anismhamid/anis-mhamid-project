function generateProjectHTML(projects, isHtmlCss) {
    const fragment = document.createDocumentFragment()

    projects.map((project) => {
        //creating a new div
        const div = document.createElement("div")
        div.className = "col-sm-12 col-md-6 col-lg-4 my-4"

        div.innerHTML = `
            <div class="parent m-auto h-100">
                <div class="card">
                    <div class="content-box">
                        <span class="card-title text-dark">${
                            project.title
                        }</span>
                        <div class="card-img">
                            <img src="${
                                project.image
                            }" class="card-img-top" alt="${project.alt}">
                        </div>
                        <button type="button" class="see-more fs-5 btn btn-outline-warning" data-bs-toggle="modal" data-bs-target="#exampleModal${
                            project.id
                        } ">
                            פרטים נוספים <img class=" img-thumbnail" src="./img/help-contents.png"></img>
                        </button>
                    </div>
                    <div class="date-box">
                        <span class="month">${project.buildYear}</span>
                    </div>
                </div>
            </div>

            <!-- Modal -->
            <div class="modal bg-dark fade" id="exampleModal${
                project.id
            }" tabindex="-1" aria-labelledby="exampleModalLabel${
            project.id
        }" aria-hidden="true">
                <div class="modal-dialog">
                    <div class="modal-content">
                        <div class="modal-header bg-dark-subtle">
                            <h1 class="modal-title text-center text-dark fs-5" id="exampleModalLabel${
                                project.id
                            }">${project.title}</h1>
                            <button type="button" class="btn-close" data-bs-dismiss="modal" aria-label="Close"></button>
                        </div>
                        <div class="modal-body">
                            <p class="lead pb-4 text-dark">${
                                project.description
                            }</p>
                            <hr class="border border-black mb-2"/>
                            ${
                                isHtmlCss
                                    ? `<div class='card-img'><img class="img-fluid" src="${project.image}" alt="${project.alt}"></div>`
                                    : `<div class='card-img'><img class="img-fluid" src="${project.image}" alt="${project.alt}"></div>`
                            }
                        </div>
                        <div class="modal-footer">
                            <button type="button" class="btn btn-outline-danger" data-bs-dismiss="modal">סגירה</button>
                            <a href="${project.pageUrl}" target="_blank">
                                <button type="button" class="btn btn-outline-primary">לעמוד הפרויקט</button>
                            </a>
                        </div>
                    </div>
                </div>
            </div>
        `

        fragment.appendChild(div)
    })

    return fragment
}

// Append HTML for HTML/CSS projects
document
    .getElementById("row1")
    .appendChild(generateProjectHTML(htmlCssProjects, false))

// Append HTML for JavaScript projects
document
    .getElementById("row2")
    .appendChild(generateProjectHTML(javaScriptProjects, true))
